public class AddingTwoNumbers {
    public static void main(String[] args) {
        // Declaring variable and initializing them to values
        int number1 = 10;
        int number2 = 20;

        // Printing the content of both variables
        System.out.println(number1 + number2);

        // I can always change the values during RunTime
        number1 = 40;
        System.out.println(number1 + number2);

        System.out.println("--------------------");

        // Create a print statement that adds two strings
        String str1 = "Rolando";
        String str2 = "Colon";
        System.out.println(str1 + " " + str2);


    }
}
